#include <stdio.h>
#include <string.h>
#include "graphplan.h"
#include "dump_search.h"

static FILE *tf = NULL;
static int  step_n = 0;
static int  first_event = 1;

/* ── helpers ─────────────────────────────────────────────── */
static void sep(void) {
    if (!first_event) fputs(",\n", tf);
    first_event = 0;
}

static void prop_id(int t, const char *name) {
    fprintf(tf, "\"prop_%d_%s\"", t, name);
}
static void act_id(int t, const char *name) {
    fprintf(tf, "\"act_%d_%s\"", t, name);
}

/* ── lifecycle ───────────────────────────────────────────── */
void trace_open(void) {
    tf = fopen("search_trace.js", "w");
    if (!tf) { perror("trace_open"); return; }
    fputs("/* Auto-generated by Graphplan – do not edit */\n", tf);
    fputs("const SEARCH_TRACE = [\n", tf);
    step_n = 0; first_event = 1;
}

void trace_close(void) {
    if (!tf) return;
    fputs("\n];\n", tf);
    fclose(tf); tf = NULL;
    puts("Search trace written to search_trace.js");
}

/* ── events ──────────────────────────────────────────────── */

/* Emitted when basicplan transitions to a new time layer */
void trace_goal_set(int time, goal_arr goals, int n) {
    if (!tf) return;
    int i; sep();
    fprintf(tf, "  {\"step\":%d,\"type\":\"goal_set\",\"time\":%d,\"goals\":[",
            step_n++, time);
    for (i = 0; i < n; i++) {
        if (i) fputs(",", tf);
        prop_id(time, goals[i]->name);
    }
    fputs("]}", tf);
}

/* Emitted when we're about to try an action for a goal */
void trace_try(int time, vertex_t op, vertex_t for_goal) {
    if (!tf) return;
    sep();
    /* time here is the action layer = time-1 in basicplan's frame */
    fprintf(tf, "  {\"step\":%d,\"type\":\"try\",\"time\":%d,\"action\":",
            step_n++, time);
    act_id(time, op->name);
    fputs(",\"for_goal\":", tf);
    prop_id(time + 1, for_goal->name);   /* goal is at time+1 layer */
    fputs("}", tf);
}

