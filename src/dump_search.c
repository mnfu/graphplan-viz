/* No action set worked at this layer */
void trace_backtrack(int time) {
    if (!tf) return;
    sep();
    fprintf(tf, "  {\"step\":%d,\"type\":\"backtrack\",\"time\":%d}", step_n++, time);
}

/* Plan found — emit which actions ended up used */
void trace_solution(int maxtime) {
    if (!tf) return;
    extern hashtable_t *op_table;
    int t;
    vertex_t v;
    int first_layer = 1; // Tracks if we need a comma between layer blocks

    sep();
    fprintf(tf, "  {\"step\":%d,\"type\":\"solution\",\"plan\":{", step_n++);

    for (t = 0; t < maxtime; t++) {
        int first_action_in_layer = 1;

        get_next(op_table[t], 0);
        while ((v = get_next(op_table[t], 1)) != NULL) {
            if (!v->used || v->is_noop) continue;

            // If this is the very first action we found for this time layer
            if (first_action_in_layer) {
                // Do we need a comma between "1":[...] and "2":[...]?
                if (!first_layer) fputs(",", tf);

                fprintf(tf, "\"%d\":[", t + 1);
                first_action_in_layer = 0;
                first_layer = 0;
            } else {
                // Standard comma between actions in the same list
                fputs(",", tf);
            }

            fprintf(tf, "\"%s\"", v->name);
        }

        // Close the array only if we actually started one for this layer
        if (!first_action_in_layer) {
            fputs("]", tf);
        }
    }
    fputs("}}", tf);
}

void trace_failure(void) {
    if (!tf) return;
    sep();
    fprintf(tf, "  {\"step\":%d,\"type\":\"failure\"}", step_n++);
}